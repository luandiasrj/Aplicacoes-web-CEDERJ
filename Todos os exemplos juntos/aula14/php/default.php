<h2>Introdução</h2>
<p>Este é o sistema de gerenciamento de bibliotecas. Ele foi projetado para servir como um exemplo de
aplicação web, incorporando todos os ensinamentos da disciplina de Programação II do curso de  Tecnologia 
em Sistemas de Computação a Distância em Computação do CEDERJ. </p>
<p>Maiores informações sobre o curso podem ser obtidas na <a href="http://www.cederj.edu.br/cecierj/tecnologia.htm">
página do curso</a>.</p>

<h2>Instruções de uso</h2>
<p>O sistema é comandado em geral pelos menus à esquerda. As opções disponíveis dependem do privilégio 
do usuário:</p>
<ul>
<li>Usuários não logados têm privilégio apenas para
consultar os livros existentes.</li>
<li>Usuários <i>membros</i> da biblioteca podem tomar livros emprestados ou reservar
livros. </li>
<li>Administradores podem gerenciar usuários e livros.</li>
</ul>
    


